def capital_letters(words: str):
    """
    :param words: поступает строка
    :return: все буквы в верхнем регистре
    """
    return words.upper()


def title_letters(words: str):
    """
    :param words: поступает строка
    :return: все слова начинаются с верхнего регистра
    """
    return words.title()
